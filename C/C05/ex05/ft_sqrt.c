int	ft_sqrt(int nb)
{
	int	i;

	i = 1;
	if (nb <= 0)
		return (0);
	while (i * i <= nb)
	{
		if (i * i == nb)
			return (i);
		if (i > 46340 || i * i > nb)
			break ;
		i++;
	}
	return (0);
}
